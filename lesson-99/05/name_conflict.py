#!/usr/bin/env python3

# Refer to https://docs.python.org/3/library/builtins.html#module-builtins

# Using reserved keywords as variable names by appending an underscore
id_ = 123
class_ = "MyClass"

print("id(id_):", id(id_))
print("id_:", id_)
print("class: reserved keyword")
print("class_:", class_)
